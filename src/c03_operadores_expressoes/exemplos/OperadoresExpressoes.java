package c03_operadores_expressoes.exemplos;

public class OperadoresExpressoes {

  public static void main(String[] args) {

    short n1 = 20;
    short n2 = 10;

    int adicao = n1 + n2;
    int subtracao = n1 - n2;
    int multiplicacao = n1 * n2;
    float divisao = n1 / n2;
    int resto = n1 % n2;

    System.out.println(n1 + " + " + n2 + " = " + (n1 + n2));

  }

}
